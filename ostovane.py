pi = 3.14
r = float(input('shoaae ra vared konid: '))
h = float(input('ertefa ra vared konid: '))
masahat_j  = 2 * pi * r * h
hajm = pi * (r ** 2) * h
masahat_k = (masahat_j) + 2 * pi * (r ** 2)
print('masahat janebi ostovane: ', masahat_j)
print('hajm ostovane:', hajm)
print('masahat koli ostovane: ', masahat_k)
